const I18nContext = createContext<I18nContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  // Try to get from localStorage, default to 'tr'
  const [language, setLanguage] = useState<Language>(() => {
      const stored = localStorage.getItem('app_language');
      return (stored === 'de' ? 'de' : 'tr');
  });

  const t = (key: string): string => {
    return translations[language][key] || key;
  };

  const formatDate = (date: string | Date, options?: Intl.DateTimeFormatOptions) => {
      const d = new Date(date);
      const locale = language === 'de' ? 'de-DE' : 'tr-TR';
      return d.toLocaleDateString(locale, options);
  };

  const handleSetLanguage = (lang: Language) => {
      setLanguage(lang);
      localStorage.setItem('app_language', lang);
  };

  return (
    <I18nContext.Provider value={{ language, setLanguage: handleSetLanguage, t, formatDate }}>
      {children}
    </I18nContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};